import Foundation

class Math {
    

    
    func addition(firstNumber : Int, secondNumber : Int) ->Int{
        
        return firstNumber + secondNumber
    }
    
    func minus(firstNumber : Int, secondNumber : Int) ->Int{
        
        return firstNumber - secondNumber
    }
    
    func times(firstNumber : Int, secondNumber : Int) ->Int{
        
        return firstNumber * secondNumber
    }

    
}
